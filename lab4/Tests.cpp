#include "Tests.h"
#include "Time.h"
#include <iostream>

void runTests() {
    try {
        cout << "Тестирование перегруженных операторов...\n";

        // тест унарных операций
        Time t1(10, 30);
        cout << "Исходное время: " << t1 << "\n";
        ++t1;
        cout << "После префиксного ++: " << t1 << "\n";
        t1++;
        cout << "После постфиксного ++: " << t1 << "\n";
        --t1;
        cout << "После префиксного --: " << t1 << "\n";
        t1--;
        cout << "После постфиксного --: " << t1 << "\n";

        // тест операций приведения типа
        cout << "Время в минутах: " << static_cast<int>(t1) << "\n";
        cout << "Булево значение времени: " << (static_cast<bool>(t1) ? "true" : "false") << "\n";

        // тест бинарных операций сравнения
        Time t2(8, 15);
        cout << "Сравнение времени:\n";
        cout << t1 << " < " << t2 << ": " << (t1 < t2 ? "true" : "false") << "\n";
        cout << t1 << " > " << t2 << ": " << (t1 > t2 ? "true" : "false") << "\n";

    }
    catch (const exception& e) {
        cerr << "Ошибка в тестах: " << e.what() << "\n";
    }
}
